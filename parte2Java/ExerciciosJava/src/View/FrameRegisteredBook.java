    private void initData() throws ClassNotFoundException, IOException {
    ArrayList<Livro> livros = (ArrayList<Livro>) Biblioteca.carregarLivros2();

    // Verificar se a lista de livros não está vazia
    if (livros != null) {
        DefaultTableModel tabelaLivros = (DefaultTableModel) tablePeople.getModel();

        // Iterar sobre os livros carregados
        for (Livro livro : livros) {
            tabelaLivros.addRow(new Object[]{
                livro.getId(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getCategoria(),
                livro.getDataLancamento()
            });
        }
    } else {
        System.out.println("Erro ao carregar biblioteca.");
    }
    }

