    private void carregaTable() {
        DefaultTableModel tbl = (DefaultTableModel) listLivros.getModel();
        tbl.setRowCount(0);

        // Consulta os livros no banco de dados usando LivroDAO
        LivroDAO livroDAO = new LivroDAO();
        ArrayList<Livro> livros = livroDAO.consultar();

        System.out.println(livros);
        for (int i = 0; i < livros.size(); i++) {
            Object[] linha = new Object[5];
            Livro temp = livros.get(i);
            linha[0] = temp.getId();
            linha[1] = temp.getTitulo();
            linha[2] = temp.getAutor();
            linha[3] = temp.getCategoria();

            SimpleDateFormat dataFormatada = new SimpleDateFormat("dd/MM/yyyy");
            linha[4] = dataFormatada.format(temp.getDataPublicacao());

            tbl.addRow(linha);
        }

    }

