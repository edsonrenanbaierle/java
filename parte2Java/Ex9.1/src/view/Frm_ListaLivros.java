    private void addLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLivroActionPerformed
        model.Livro novoLivro = new model.Livro();
        Frm_Cadastro cadastro = null;
        try {
            cadastro = new Frm_Cadastro(novoLivro, biblioteca.getLstLivros());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        cadastro.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                try {
                    if (novoLivro.getId() != 0
                            && novoLivro.getTitulo() != null
                            && novoLivro.getAutor() != null
                            && novoLivro.getCategoria() != null
                            && novoLivro.getDataPublicacao() != null) {
                        biblioteca.addLivro(novoLivro);
                        carregaTable();
                        biblioteca.salvar();
                    }
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
        cadastro.setTitle("Cadastro de livro");
        cadastro.setVisible(true);
    }//GEN-LAST:event_addLivroActionPerformed

    private void removeLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeLivroActionPerformed

        for (int linha : listLivros.getSelectedRows()) {
            int idLivro = Integer.parseInt(listLivros.getValueAt(linha, 0).toString());
            LivroDAO livroDAO = new LivroDAO();
            livroDAO.removerLivro(idLivro); // Chama o método removerLivro do LivroDAO
        }

        carregaTable();


    }//GEN-LAST:event_removeLivroActionPerformed

